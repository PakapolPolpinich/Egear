void Spi_tranfer(uint8_t addr,uint8_t data){
	uint16_t writedata = (addr<<8)|data;
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4,0);
	HAL_SPI_Transmit(&hspi1, (uint8_t*)&writedata,1,100);
	HAL_GPIO_WritePin(GPIOA, GPIO_PIN_4,1);
}

void Dotmatrix_init(void){
	Spi_tranfer(0x0C, 0x00);  //shutdown
	Spi_tranfer(0x09, 0x00);  //set no decode
	Spi_tranfer(0x0A, 0x01);  //set intensity  3/32
	Spi_tranfer(0x0B, 0x07);  //scanlimit display digit0-7
	Spi_tranfer(0x0C, 0x01);  //normal operation
}



